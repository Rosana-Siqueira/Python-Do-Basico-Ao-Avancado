# Realizar a média das notas de 4 alunos.

notaPedro = float(input('digite a nota de Pedro: '))
notaPaula = float(input('Digite a nota de Paula: '))
notaPatricia = float(input('Digite a nota de Patricia: '))
notaPablo = float(input('Digite a nota de Pablo: '))

media = (notaPedro + notaPaula + notaPatricia + notaPablo) / 4
print(f'Média das notas: {media}')
